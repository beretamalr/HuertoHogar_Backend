package com.duoc.proyecto.tienda_backend.model;

import jakarta.persistence.*;
import java.util.List;

@Entity
@Table(name = "usuario")
public class Usuario {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    private String nombre;
    private String email;
    private String password;
    private String avatar;
    private String rol; // ADMIN, VENDEDOR, CLIENTE

    @OneToMany(mappedBy = "usuario", cascade = CascadeType.ALL)
    private List<Boleta> boletas;

    public Usuario() {}

    public Usuario(Long id, String nombre, String email, String password, String avatar, String rol) {
        this.id = id;
        this.nombre = nombre;
        this.email = email;
        this.password = password;
        this.avatar = avatar;
        this.rol = rol;
    }

    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }

    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getPassword() { return password; }
    public void setPassword(String password) { this.password = password; }

    public String getAvatar() { return avatar; }
    public void setAvatar(String avatar) { this.avatar = avatar; }

    public String getRol() { return rol; }
    public void setRol(String rol) { this.rol = rol; }

    public List<Boleta> getBoletas() { return boletas; }
    public void setBoletas(List<Boleta> boletas) { this.boletas = boletas; }
}